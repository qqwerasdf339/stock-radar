export function normalizeStockCode(symbol = "") {
  return String(symbol)
    .trim()
    .toUpperCase()
    .replace(".TW", "")
    .replace(".TWO", "");
}

export function getCachedTwStockNames() {
  if (typeof localStorage === "undefined") return {};

  try {
    return JSON.parse(localStorage.getItem(TW_STOCK_NAME_CACHE_KEY) || "{}");
  } catch {
    return {};
  }
}

export function saveCachedTwStockNames(map) {
  if (typeof localStorage === "undefined") return;

  localStorage.setItem(TW_STOCK_NAME_CACHE_KEY, JSON.stringify(map));
}

function pickCode(row) {
  return (
    row.Code ||
    row.code ||
    row["公司代號"] ||
    row["有價證券代號"] ||
    row["證券代號"] ||
    row.SecuritiesCompanyCode ||
    row.SecuritiesCode ||
    ""
  );
}

function pickName(row) {
  return (
    row.Name ||
    row.name ||
    row["公司簡稱"] ||
    row["公司名稱"] ||
    row["有價證券名稱"] ||
    row["證券名稱"] ||
    row.CompanyName ||
    row.SecuritiesName ||
    ""
  );
}

export async function fetchTwStockNamesFromOpenApi() {
  const entries = {};

  for (const source of TW_STOCK_NAME_SOURCES) {
    try {
      const res = await fetch(source.url, { cache: "no-store" });
      if (!res.ok) continue;

      const rows = await res.json();

      for (const row of rows || []) {
        const code = normalizeStockCode(pickCode(row));
        const name = String(pickName(row) || "").trim();

        if (!/^\d{4,6}[A-Z]?$/.test(code)) continue;
        if (!name) continue;

        entries[code] = name;
      }
    } catch (err) {
      console.warn("TW stock names source failed:", source.market, err);
    }
  }

  if (Object.keys(entries).length > 0) {
    const finalMap = {
      ...TW_STOCK_NAMES_FALLBACK,
      ...entries,
    };

    saveCachedTwStockNames(finalMap);
    return finalMap;
  }

  return {
    ...TW_STOCK_NAMES_FALLBACK,
    ...getCachedTwStockNames(),
  };
}

export function getTwStockNameSync(symbol, fallbackName = "") {
  const code = normalizeStockCode(symbol);
  const cached = getCachedTwStockNames();

  return (
    cached[code] ||
    TW_STOCK_NAMES_FALLBACK[code] ||
    fallbackName ||
    code
  );
}
